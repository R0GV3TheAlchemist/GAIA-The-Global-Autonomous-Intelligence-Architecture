def schumann_coherence_check(
    grid: Any,  # GridDesign from grid_design.py (avoid circular import)
    readings: Optional[list[SchumannReading]] = None,
    operator_proximity_m: float = 1.0,
    tolerance_hz: float = SCHUMANN_TOLERANCE_HZ,
) -> SchumannCoherenceResult:
    """
    Score a crystal grid's Schumann resonance coherence.

    If readings is None, the grid's crystal coverage is assessed against
    the canonical Schumann frequencies (structural coherence check).

    If readings are provided (live or simulated), each reading is scored
    against its canonical harmonic and the composite score reflects actual
    measured alignment.

    Operator biological safety is checked for every Schumann-band node
    (Selenite proximity rule from EMBODIMENT_LAYER.md).

    Args:
        grid:                 GridDesign from grid_design.py
        readings:             optional list of SchumannReading measurements
        operator_proximity_m: Operator distance for safety checks (metres)
        tolerance_hz:         Gaussian scoring tolerance (default 0.5 Hz)

    Returns:
        SchumannCoherenceResult with composite score, GAIAState signal dict,
        recommended crystals, and safety flags
    """
    # ── 1. Find Schumann-band nodes in the grid ──────────────────────────────
    schumann_nodes: list[int] = []
    node_coverage:  dict[int, list[int]] = {h: [] for h in SCHUMANN_FUNDAMENTALS}

    for node in grid.nodes:
        crystal = CRYSTAL_DATABASE.get(node.crystal_key)
        if crystal is None:
            continue
        band_min, band_max = crystal.resonant_band_hz
        for harmonic, freq_hz in SCHUMANN_FUNDAMENTALS.items():
            if band_min <= freq_hz <= band_max:
                node_coverage[harmonic].append(node.node_id)
                if node.node_id not in schumann_nodes:
                    schumann_nodes.append(node.node_id)

    # ── 2. Score each harmonic ────────────────────────────────────────────────
    harmonic_scores: dict[int, float] = {}

    if readings:
        # Live / simulated readings: score against canonical frequencies
        reading_map: dict[int, list[SchumannReading]] = {h: [] for h in SCHUMANN_FUNDAMENTALS}
        for r in readings:
            reading_map[r.harmonic].append(r)

        for harmonic in SCHUMANN_FUNDAMENTALS:
            harmo_readings = reading_map[harmonic]
            if not harmo_readings:
                harmonic_scores[harmonic] = 0.0
            else:
                # Best reading for this harmonic (highest amplitude × frequency score)
                best = max(
                    harmo_readings,
                    key=lambda r: r.amplitude * _score_harmonic(r.frequency_hz, harmonic, tolerance_hz)
                )
                freq_score = _score_harmonic(best.frequency_hz, harmonic, tolerance_hz)
                harmonic_scores[harmonic] = best.amplitude * freq_score
    else:
        # Structural check: does the grid have crystal coverage at this harmonic?
        for harmonic in SCHUMANN_FUNDAMENTALS:
            if node_coverage[harmonic]:
                # Score based on the highest Q crystal covering this harmonic
                covered_keys = [
                    grid.nodes[nid].crystal_key
                    for nid in node_coverage[harmonic]
                    if nid < len(grid.nodes)
                ]
                if covered_keys:
                    best_q = max(
                        CRYSTAL_DATABASE[k].q_factor
                        for k in covered_keys
                        if k in CRYSTAL_DATABASE
                    )
                    # Normalise Q to 0-1 score: Q=100,000 → 1.0, Q=5,000 → 0.05
                    harmonic_scores[harmonic] = min(1.0, best_q / 100_000.0)
                else:
                    harmonic_scores[harmonic] = 0.0
            else:
                harmonic_scores[harmonic] = 0.0

    # ── 3. Composite score (weighted: harmonic 1 counts 3×, others 1×) ────────
    weights = {1: 3.0, 2: 1.5, 3: 1.0, 4: 1.0, 5: 1.0, 6: 0.75, 7: 0.75}
    total_weight  = sum(weights.values())
    weighted_sum  = sum(harmonic_scores[h] * weights[h] for h in SCHUMANN_FUNDAMENTALS)
    composite     = weighted_sum / total_weight

    # ── 4. Active / missing harmonics ────────────────────────────────────────
    active_harmonics  = [h for h, s in harmonic_scores.items() if s >= 0.5]
    missing_harmonics = [h for h, s in harmonic_scores.items() if s < 0.1]

    # ── 5. Recommend crystals for gaps ───────────────────────────────────────
    recommended = _recommend_crystals(missing_harmonics)

    # ── 6. Biological safety checks for all Schumann nodes ───────────────────
    safety_flags: list[str] = []
    for node in grid.nodes:
        if node.node_id in schumann_nodes:
            crystal = CRYSTAL_DATABASE.get(node.crystal_key)
            if crystal and crystal.biological_interface == "schumann_coherence":
                safety = biological_safety_rating(
                    node.crystal_key, operator_proximity_m
                )
                if safety["flags"]:
                    safety_flags.extend(safety["flags"])

    # ── 7. Build GAIAState signal dict (consumed by gaia_state_bridge.py) ─────
    dominant_harmonic = max(harmonic_scores, key=lambda h: harmonic_scores[h])
    gaia_state_signal: dict[str, Any] = {
        "coherence_delta":   round(composite - 0.5, 4),   # nudge relative to neutral
        "coherence_abs":     round(composite, 4),
        "schumann_active":   len(active_harmonics) >= 1,
        "dominant_harmonic": dominant_harmonic,
        "harmonic_scores":   {str(h): round(s, 4) for h, s in harmonic_scores.items()},
        "timestamp":         time.time(),
        "source":            "live" if readings else "structural",
    }

    return SchumannCoherenceResult(
        composite_score      = round(composite, 4),
        harmonic_scores      = harmonic_scores,
        active_harmonics     = active_harmonics,
        schumann_nodes       = schumann_nodes,
        missing_harmonics    = missing_harmonics,
        recommended_crystals = recommended,
        safety_flags         = safety_flags,
        gaia_state_signal    = gaia_state_signal,
    )


