class SchumannMonitor:
    """
    Stateful Schumann resonance monitor for continuous operation.

    Call update() on each new reading. The monitor maintains a rolling
    window of the last N readings and emits a GAIAState signal dict
    on every update.

    Usage in GAIA-OS backend:
        monitor = SchumannMonitor(window_size=12)
        # On each sensor tick:
        signal = monitor.update(SchumannReading(1, 7.84, 0.90))
        state  = apply_schumann_signal(state, signal)
    """

    def __init__(self, window_size: int = 12) -> None:
        if window_size < 1:
            raise ValueError(f"window_size must be >= 1, got {window_size}")
        self.window_size  = window_size
        self._readings:   list[SchumannReading] = []
        self._last_signal: Optional[dict[str, Any]] = None

    def update(self, reading: SchumannReading) -> dict[str, Any]:
        """
        Accept a new SchumannReading and return an updated GAIAState signal.

        The signal dict is the same format as SchumannCoherenceResult.gaia_state_signal
        and can be passed directly to apply_schumann_signal() in gaia_state_bridge.py.
        """
        self._readings.append(reading)
        # Trim to window
        if len(self._readings) > self.window_size * len(SCHUMANN_FUNDAMENTALS):
            self._readings = self._readings[-(self.window_size * len(SCHUMANN_FUNDAMENTALS)):]

        # Score
        harmonic_scores: dict[int, float] = {h: 0.0 for h in SCHUMANN_FUNDAMENTALS}
        for r in self._readings:
            fs = _score_harmonic(r.frequency_hz, r.harmonic)
            score = r.amplitude * fs
            if score > harmonic_scores[r.harmonic]:
                harmonic_scores[r.harmonic] = score

        weights      = {1: 3.0, 2: 1.5, 3: 1.0, 4: 1.0, 5: 1.0, 6: 0.75, 7: 0.75}
        total_weight = sum(weights.values())
        composite    = sum(harmonic_scores[h] * weights[h] for h in SCHUMANN_FUNDAMENTALS) / total_weight
        active       = [h for h, s in harmonic_scores.items() if s >= 0.5]
        dominant     = max(harmonic_scores, key=lambda h: harmonic_scores[h])

        signal: dict[str, Any] = {
            "coherence_delta":   round(composite - 0.5, 4),
            "coherence_abs":     round(composite, 4),
            "schumann_active":   len(active) >= 1,
            "dominant_harmonic": dominant,
            "harmonic_scores":   {str(h): round(harmonic_scores[h], 4) for h in SCHUMANN_FUNDAMENTALS},
            "timestamp":         time.time(),
            "source":            "live",
        }
        self._last_signal = signal
        return signal

    @property
    def last_signal(self) -> Optional[dict[str, Any]]:
        return self._last_signal

    @property
    def reading_count(self) -> int:
        return len(self._readings)

    def reset(self) -> None:
        self._readings    = []
        self._last_signal = None


