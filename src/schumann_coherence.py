def schumann_grid_audit(grid: Any) -> dict[str, Any]:
    """
    Audit a GridDesign for Schumann harmonic coverage.

    Reports which nodes cover which harmonics and flags gaps.

    Args:
        grid: GridDesign from grid_design.py

    Returns:
        {
            'covered_harmonics': {harmonic: [node_ids]},
            'uncovered_harmonics': [harmonic_numbers],
            'schumann_node_count': int,
            'coverage_pct': float 0-100,
            'audit_lines': [str],
        }
    """
    covered:   dict[int, list[int]] = {h: [] for h in SCHUMANN_FUNDAMENTALS}
    audit_lines: list[str] = []

    for node in grid.nodes:
        crystal = CRYSTAL_DATABASE.get(node.crystal_key)
        if crystal is None:
            continue
        band_min, band_max = crystal.resonant_band_hz
        covered_here: list[int] = []
        for harmonic, freq_hz in SCHUMANN_FUNDAMENTALS.items():
            if band_min <= freq_hz <= band_max:
                covered[harmonic].append(node.node_id)
                covered_here.append(harmonic)
        if covered_here:
            audit_lines.append(
                f"  Node {node.node_id:02d} [{node.crystal_name:<20}]  "
                f"covers harmonics: {covered_here}"
            )

    uncovered = [h for h, nodes in covered.items() if not nodes]
    all_schumann_nodes: set[int] = set()
    for nodes in covered.values():
        all_schumann_nodes.update(nodes)

    coverage_pct = 100.0 * (len(SCHUMANN_FUNDAMENTALS) - len(uncovered)) / len(SCHUMANN_FUNDAMENTALS)

    if uncovered:
        audit_lines.append(f"  ⚠  Uncovered harmonics: {uncovered}")
    else:
        audit_lines.append("  ✅ All 7 Schumann harmonics covered.")

    return {
        "covered_harmonics":  covered,
        "uncovered_harmonics": uncovered,
        "schumann_node_count": len(all_schumann_nodes),
        "coverage_pct":        round(coverage_pct, 1),
        "audit_lines":         audit_lines,
    }


