def simulate_schumann_episode(
    n_steps: int = 24,
    base_amplitude: float = 0.85,
    drift_sigma: float = 0.05,
    include_all_harmonics: bool = True,
    seed: Optional[int] = None,
) -> dict[str, Any]:
    """
    Simulate a time-series of Schumann readings over n_steps.

    Each step produces one SchumannReading per harmonic (or per harmonic 1
    only if include_all_harmonics=False). Amplitude drifts as a random walk
    with drift_sigma standard deviation per step, clamped to [0.1, 1.0].

    The output episode dict is ready for logging to GAIAState history.

    Args:
        n_steps:              number of time steps (default 24 = 1 day at 1h interval)
        base_amplitude:       starting amplitude for all harmonics (default 0.85)
        drift_sigma:          std dev of Gaussian amplitude drift per step
        include_all_harmonics: if False, only simulates harmonic 1 (7.83 Hz)
        seed:                 random seed for reproducibility

    Returns:
        {
            'n_steps': int,
            'steps': [
                {
                    'step': int,
                    'readings': [SchumannReading, ...],
                    'composite_score': float,
                    'active_harmonics': [int, ...],
                }
            ],
            'mean_composite': float,
            'min_composite': float,
            'max_composite': float,
            'gaia_state_history': [signal_dict, ...],
        }
    """
    import random
    rng = random.Random(seed)

    harmonics_to_sim = list(SCHUMANN_FUNDAMENTALS.keys()) if include_all_harmonics else [1]

    # Initialise per-harmonic amplitudes
    amplitudes: dict[int, float] = {h: base_amplitude for h in harmonics_to_sim}

    steps: list[dict[str, Any]] = []
    gaia_history: list[dict[str, Any]] = []
    composites: list[float] = []

    for step in range(n_steps):
        readings: list[SchumannReading] = []
        for h in harmonics_to_sim:
            # Drift amplitude
            amplitudes[h] = max(0.1, min(1.0, amplitudes[h] + rng.gauss(0, drift_sigma)))
            # Small frequency jitter (±0.3 Hz)
            freq_jitter = rng.gauss(0, 0.1)
            readings.append(SchumannReading(
                harmonic     = h,
                frequency_hz = SCHUMANN_FUNDAMENTALS[h] + freq_jitter,
                amplitude    = round(amplitudes[h], 4),
                timestamp    = time.time() + step * 3600,
                source       = "simulated",
            ))

        # Score this step
        harmonic_scores: dict[int, float] = {}
        for r in readings:
            fs = _score_harmonic(r.frequency_hz, r.harmonic)
            harmonic_scores[r.harmonic] = r.amplitude * fs
        # Fill unscored harmonics with 0
        for h in SCHUMANN_FUNDAMENTALS:
            if h not in harmonic_scores:
                harmonic_scores[h] = 0.0

        weights      = {1: 3.0, 2: 1.5, 3: 1.0, 4: 1.0, 5: 1.0, 6: 0.75, 7: 0.75}
        total_weight = sum(weights.values())
        composite    = sum(harmonic_scores[h] * weights[h] for h in SCHUMANN_FUNDAMENTALS) / total_weight
        composites.append(composite)

        active = [h for h, s in harmonic_scores.items() if s >= 0.5]
        signal: dict[str, Any] = {
            "coherence_delta":   round(composite - 0.5, 4),
            "coherence_abs":     round(composite, 4),
            "schumann_active":   len(active) >= 1,
            "dominant_harmonic": max(harmonic_scores, key=lambda h: harmonic_scores[h]),
            "harmonic_scores":   {str(h): round(harmonic_scores[h], 4) for h in SCHUMANN_FUNDAMENTALS},
            "timestamp":         time.time() + step * 3600,
            "source":            "simulated",
            "step":              step,
        }
        gaia_history.append(signal)
        steps.append({
            "step":             step,
            "readings":         readings,
            "composite_score":  round(composite, 4),
            "active_harmonics": active,
        })

    return {
        "n_steps":             n_steps,
        "steps":               steps,
        "mean_composite":      round(sum(composites) / len(composites), 4),
        "min_composite":       round(min(composites), 4),
        "max_composite":       round(max(composites), 4),
        "gaia_state_history":  gaia_history,
    }


