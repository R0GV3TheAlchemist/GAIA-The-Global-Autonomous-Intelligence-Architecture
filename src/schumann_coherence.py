def _score_harmonic(
    measured_hz: float,
    harmonic: int,
    tolerance_hz: float = SCHUMANN_TOLERANCE_HZ,
) -> float:
    """
    Score a measured frequency against a Schumann harmonic.

    Uses a Gaussian window centred on the harmonic's canonical frequency.
    Score = exp( -(delta_hz)^2 / (2 * tolerance_hz^2) )

    Full score (1.0) when measured == canonical.
    ~0.607 at 1 sigma (tolerance_hz) deviation.
    ~0.135 at 2 sigma deviation.

    Args:
        measured_hz:  measured frequency in Hz
        harmonic:     Schumann harmonic number (1–7)
        tolerance_hz: 1-sigma tolerance in Hz (default 0.5 Hz)

    Returns:
        float in [0.0, 1.0]
    """
    canonical = SCHUMANN_FUNDAMENTALS[harmonic]
    delta     = measured_hz - canonical
    return math.exp(-(delta ** 2) / (2 * tolerance_hz ** 2))


def _recommend_crystals(
    missing_harmonics: list[int],
    max_per_harmonic: int = 2,
) -> list[str]:
    """
    Suggest crystal keys to fill gaps in Schumann harmonic coverage.

    For each missing harmonic, queries select_resonator() at the harmonic
    frequency, filtering for biological_interface phononic_class and
    excluding toxic crystals.

    Returns de-duplicated list of crystal keys sorted by Q descending.
    """
    seen:     set[str]  = set()
    results:  list[tuple[float, str]] = []

    for harmonic in missing_harmonics:
        freq_hz = SCHUMANN_FUNDAMENTALS[harmonic]
        candidates = select_resonator(
            freq_hz,
            min_q=5_000,
            exclude_toxic=True,
        )
        count = 0
        for c in candidates:
            # Resolve candidate name to db key
            for db_key, db_crystal in CRYSTAL_DATABASE.items():
                if db_crystal.name == c["name"] and db_key not in seen:
                    seen.add(db_key)
                    results.append((c["q_factor"], db_key))
                    count += 1
                    break
            if count >= max_per_harmonic:
                break

    # Sort by Q descending, return keys only
    results.sort(key=lambda x: x[0], reverse=True)
    return [key for (_, key) in results]


