@dataclass
class SchumannReading:
    """
    A single Schumann resonance measurement.

    In live operation, these come from a magnetometer or ELF receiver.
    In simulation, they are generated by simulate_schumann_episode().

    Attributes:
        harmonic:      which Schumann harmonic (1–7)
        frequency_hz:  measured peak frequency in Hz
        amplitude:     relative amplitude 0.0–1.0 (1.0 = nominal)
        timestamp:     Unix timestamp of the reading
        source:        'live' | 'simulated' | 'manual'
    """
    harmonic:     int
    frequency_hz: float
    amplitude:    float
    timestamp:    float = field(default_factory=time.time)
    source:       str   = "simulated"

    def __post_init__(self) -> None:
        if self.harmonic not in SCHUMANN_FUNDAMENTALS:
            raise ValueError(
                f"harmonic must be 1–7, got {self.harmonic}. "
                f"Valid harmonics: {list(SCHUMANN_FUNDAMENTALS.keys())}"
            )
        if not (0.0 <= self.amplitude <= 1.0):
            raise ValueError(f"amplitude must be 0.0–1.0, got {self.amplitude}")


@dataclass
class SchumannCoherenceResult:
    """
    Full Schumann coherence assessment for a crystal grid or live readings.

    This is the primary output of schumann_coherence_check().
    The gaia_state_signal dict is consumed directly by apply_schumann_signal()
    in gaia_state_bridge.py to update GAIAState.coherence.

    Attributes:
        composite_score:     overall coherence 0.0–1.0 (weighted mean of harmonics)
        harmonic_scores:     {harmonic_number: score_0_to_1}
        active_harmonics:    list of harmonic numbers with score >= 0.5
        schumann_nodes:      list of node_ids in the grid with Schumann coverage
        missing_harmonics:   harmonic numbers with no crystal coverage
        recommended_crystals: list of crystal keys to improve coverage
        safety_flags:        biological safety warnings
        gaia_state_signal:   ready-to-merge dict for GAIAState
            {
                'coherence_delta': float,   # how much to nudge coherence
                'coherence_abs':   float,   # absolute coherence value
                'schumann_active': bool,
                'dominant_harmonic': int,
                'timestamp': float,
            }
    """
    composite_score:      float
    harmonic_scores:      dict[int, float]
    active_harmonics:     list[int]
    schumann_nodes:       list[int]
    missing_harmonics:    list[int]
    recommended_crystals: list[str]
    safety_flags:         list[str]
    gaia_state_signal:    dict[str, Any]

    def summary(self) -> str:
        lines = [
            f"SchumannCoherenceResult",
            f"  Composite score : {self.composite_score:.3f}",
            f"  Active harmonics: {self.active_harmonics}",
            f"  Missing coverage: {self.missing_harmonics}",
            f"  Schumann nodes  : {self.schumann_nodes}",
        ]
        if self.recommended_crystals:
            lines.append(f"  Recommended     : {self.recommended_crystals[:3]}")
        if self.safety_flags:
            lines.append(f"  Safety flags    : {self.safety_flags[:2]}")
        lines.append(f"  GAIAState signal: {self.gaia_state_signal}")
        return "\n".join(lines)


